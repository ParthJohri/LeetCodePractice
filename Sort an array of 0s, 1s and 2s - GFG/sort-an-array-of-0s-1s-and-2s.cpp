class Solution
{
    public:
    void sort012(int a[], int n)
    {
        // code here 
        int low=0,high=n-1,temp=0;
        while(temp<=high){
            if(a[temp]==1) temp++;
            else if(a[temp]==0){
                swap(a[temp],a[low]);
                low++;
                temp++;
            }
            else if(a[temp]==2){
                swap(a[temp],a[high]);
                high--;
            }
        }
    }
    
};

