//User function template for C++
class Solution{
public:
	int rowWithMax1s(vector<vector<int> > arr, int n, int m) {
	    // code here
	    int c=0,ans=-1,x=0,mx=INT_MIN;
	    for(auto i:arr){
	        c=0;
	        for(auto j:i){
	            if(j==1) ++c;
	        }
	        if(c!=0 && c>mx) {ans=x;mx=c;}
	        x++;
	    }
	    return ans;
	}

};

